import fs from 'fs'

import { buildConsensusTally, computeConsensus } from '@jbrowse/alignments-core'
import { firstValueFrom } from 'rxjs'
import { toArray } from 'rxjs/operators'

import BamAdapter from '../BamAdapter/BamAdapter.ts'
import configSchema from '../BamAdapter/configSchema.ts'

import type { ConsensusFeature } from '@jbrowse/alignments-core'

// Reference ctgA sequence, loaded once and sliced per region.
function loadCtgA() {
  const fa = fs.readFileSync(
    require.resolve('../../test_data/volvox.fa'),
    'utf8',
  )
  const lines = fa.split('\n')
  const seq: string[] = []
  let inCtgA = false
  for (const line of lines) {
    if (line.startsWith('>')) {
      inCtgA = line.slice(1).split(/\s/)[0] === 'ctgA'
    } else if (inCtgA) {
      seq.push(line.trim())
    }
  }
  return seq.join('')
}

function makeAdapter() {
  return new BamAdapter(
    configSchema.create({
      bamLocation: {
        localPath: require.resolve('../../test_data/volvox-sorted.bam'),
        locationType: 'LocalPathLocation',
      },
      index: {
        location: {
          localPath: require.resolve('../../test_data/volvox-sorted.bam.bai'),
          locationType: 'LocalPathLocation',
        },
      },
    }),
  )
}

