// ctgA:15001-15200 contains a read-supported insertion that wins its column;
// generated with the same command minus `--show-ins no`. Exercises the real
// forEachMismatch inserted-base decoding end to end.
const GOLDEN_WITH_INSERTIONS = {
  '15000-15200':
    'CCACATTCAGCTCTCGGTAACATGGGAGGCTTGTGGTTGCACCGTAAAAGGGGGATAGCCCATCCATCCTGTAAACCAACAATCGCGCGTAGCTTAATACGCTCACATTAGACATTCGATCGAGAGACCTGGTTTCAAGAGCCTTCCCTTTTGCTTTAGTGGGCCCAAATCGCAACCCTGCTCCCCTCCCTTACGCCTTAT',
}

test.each(Object.keys(GOLDEN_WITH_INSERTIONS))(
  'consensus with insertions matches samtools for ctgA %s',
  async key => {
    const [start, end] = key.split('-').map(Number) as [number, number]
    const ctgA = loadCtgA()
    const adapter = makeAdapter()
    const region = { assemblyName: 'volvox', refName: 'ctgA', start, end }
    const features = (await firstValueFrom(
      adapter.getFeatures(region).pipe(toArray()),
    )) as unknown as ConsensusFeature[]

    const tally = buildConsensusTally(features, region)
    const consensus = computeConsensus(ctgA.slice(start, end), tally, {
      includeInsertions: true,
    })

    expect(consensus).toBe(
      GOLDEN_WITH_INSERTIONS[key as keyof typeof GOLDEN_WITH_INSERTIONS],
    )
  },
)

