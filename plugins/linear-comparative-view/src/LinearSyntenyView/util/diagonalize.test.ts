  test('r64 vs yjm1447: swaps V and IX to match r64 chromosome order', async () => {
    const result = await diagonalizeRegions(
      r64VsYjm1447Alignments,
      r64RegionsOrder,
      yjm1447RegionsFaiOrder,
    )

    const names = result.newRegions.map(r => r.refName)

    // V (maps to NC_001137.3 = chrV) should come before IX (maps to NC_001141.2 = chrIX)
    expect(names.indexOf('V')).toBeLessThan(names.indexOf('IX'))

    // MT (maps to NC_001224.1 = last r64 chromosome) should be after XVI
    expect(names.indexOf('MT')).toBeGreaterThan(names.indexOf('XVI'))

    // Full expected order: I II III IV V VI VII VIII IX X XI XII XIII XIV XV XVI MT
    expect(names).toEqual([
      'I',
      'II',
      'III',
      'IV',
      'V',
      'VI',
      'VII',
      'VIII',
      'IX',
      'X',
      'XI',
      'XII',
      'XIII',
      'XIV',
      'XV',
      'XVI',
      'MT',
    ])

    // No chromosome should be reversed (all align in + orientation)
    for (const region of result.newRegions) {
      expect(region.reversed).toBe(false)
    }
  })

