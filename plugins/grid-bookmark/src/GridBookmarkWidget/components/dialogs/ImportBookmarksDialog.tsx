import React, { useState } from 'react'
import { observer } from 'mobx-react'
import { getSession } from '@jbrowse/core/util'
import { FileLocation, isSessionWithShareURL } from '@jbrowse/core/util/types'
import { ErrorMessage, FileSelector } from '@jbrowse/core/ui'
import {
  Accordion,
  AccordionSummary,
  AccordionDetails,
  Button,
  DialogContent,
  DialogActions,
  FormLabel,
  FormControl,
  FormControlLabel,
  RadioGroup,
  Radio,
  TextField,
  Typography,
} from '@mui/material'
import { openLocation } from '@jbrowse/core/util/io'
import { Dialog, AssemblySelector } from '@jbrowse/core/ui'
import { makeStyles } from 'tss-react/mui'

// icons
import ImportIcon from '@mui/icons-material/Publish'
import ExpandMoreIcon from '@mui/icons-material/ExpandMore'

// locals
import { GridBookmarkModel } from '../../model'
import { fromUrlSafeB64 } from '../../utils'
import { readSessionFromDynamo } from '../../sessionSharing'

const useStyles = makeStyles()(theme => ({
  expandIcon: {
    color: theme.palette.tertiary?.contrastText || '#fff',
  },
  minWidth: {
    minWidth: 500,
  },
}))

async function getBookmarksFromShareLink(shareLink: string, shareURL: string) {
  const defaultURL = 'https://share.jbrowse.org/api/v1/'
  const urlParams = new URL(shareLink)
  const sessionQueryParam = urlParams.searchParams.get('bookmarks')
  const password = urlParams.searchParams.get('password')
  const decryptedSession = await readSessionFromDynamo(
    `${shareURL ?? defaultURL}load`,
    sessionQueryParam || '',
    password || '',
  )

  const sharedSession = JSON.parse(await fromUrlSafeB64(decryptedSession))
  return sharedSession.sharedBookmarks
}

async function getBookmarksFromTSVFile(location: FileLocation) {
  const data = await openLocation(location).readFile('utf8')
  let lines = data.split(/\n|\r\n|\r/).filter(f => !!f.trim())
  if (lines[0].startsWith('chrom')) {
    lines = lines.slice(1)
  }
  return lines
    .filter(f => !f.startsWith('#'))
    .map(line => {
      const [refName, start, end, label, assemblyName] = line.split('\t')
      return {
        assemblyName,
        refName,
        start: +start,
        end: +end,
        label: label === '.' ? undefined : label,
      }
    })
}

async function getBookmarksFromBEDFile(
  location: FileLocation,
  selectedAsm?: string,
) {
  const data = await openLocation(location).readFile('utf8')
  let lines = data.split(/\n|\r\n|\r/).filter(f => !!f.trim())
  if (lines[0].startsWith('chrom')) {
    lines = lines.slice(1)
  }

  return lines
    .filter(f => !f.startsWith('#'))
    .map(line => {
      const [refName, start, end, label, assembly] = line.split('\t')
      return {
        assemblyName: assembly ?? selectedAsm,
        refName,
        start: +start,
        end: +end,
        label: label === '.' ? undefined : label,
      }
    })
}

const ImportBookmarksDialog = observer(function ({
  onClose,
  model,
}: {
  onClose: () => void
  model: GridBookmarkModel
}) {
  const { classes } = useStyles()
  const [location, setLocation] = useState<FileLocation>()
  const [error, setError] = useState<unknown>()
  const [fileType, setFileType] = useState('tsv')
  const [shareLink, setShareLink] = useState('')
  const session = getSession(model)
  const { assemblyNames } = session
  const [selectedAsm, setSelectedAsm] = useState(assemblyNames[0])
  const [expanded, setExpanded] = useState<
    'shareLinkAccordion' | 'fileAccordion'
  >('shareLinkAccordion')

  return (
    <Dialog open onClose={onClose} maxWidth="xl" title="Import bookmarks">
      <DialogContent className={classes.minWidth}>
        <Accordion
          expanded={expanded === 'shareLinkAccordion'}
          onChange={() => setExpanded('shareLinkAccordion')}
        >
          <AccordionSummary
            expandIcon={<ExpandMoreIcon className={classes.expandIcon} />}
          >
            <Typography
              style={{ display: 'flex', alignItems: 'center', gap: '5px' }}
            >
              Import from share link
            </Typography>
          </AccordionSummary>
          <AccordionDetails>
            <Typography>
              Paste a bookmark share link generated by the 'Share' button from
              the bookmarks widget
            </Typography>
            <TextField
              label="Enter Share URL"
              variant="outlined"
              fullWidth
              value={shareLink}
              onChange={e => setShareLink(e.target.value)}
            />
          </AccordionDetails>
        </Accordion>
        <Accordion
          expanded={expanded === 'fileAccordion'}
          onChange={() => setExpanded('fileAccordion')}
        >
          <AccordionSummary
            expandIcon={<ExpandMoreIcon className={classes.expandIcon} />}
          >
            <Typography>Import from file</Typography>
          </AccordionSummary>
          <AccordionDetails>
            <FormControl>
              <FormLabel>File type</FormLabel>
              <RadioGroup
                value={fileType}
                onChange={event => setFileType(event.target.value)}
              >
                <FormControlLabel value="bed" control={<Radio />} label="BED" />
                <FormControlLabel value="tsv" control={<Radio />} label="TSV" />
              </RadioGroup>
            </FormControl>
            <FileSelector
              location={location}
              setLocation={setLocation}
              name="File"
              description={`Select ${fileType} file to import. ${fileType === 'tsv' ? 'Columns are chrom, start, end, label, assemblyName' : ''}`}
            />
            {fileType === 'bed' ? (
              <AssemblySelector
                onChange={val => setSelectedAsm(val)}
                helperText={`Select the assembly for BED file`}
                session={session}
                selected={selectedAsm}
              />
            ) : null}
          </AccordionDetails>
        </Accordion>
        {error ? <ErrorMessage error={error} /> : null}
      </DialogContent>
      <DialogActions>
        <Button variant="contained" color="secondary" onClick={onClose}>
          Cancel
        </Button>
        <Button
          data-testid="dialogImport"
          variant="contained"
          color="primary"
          disabled={!location && !shareLink}
          startIcon={<ImportIcon />}
          onClick={async () => {
            try {
              if (expanded === 'fileAccordion' && location) {
                if (fileType === 'BED') {
                  model.importBookmarks(
                    await getBookmarksFromBEDFile(location, selectedAsm),
                  )
                } else if (fileType === 'TSV') {
                  model.importBookmarks(await getBookmarksFromTSVFile(location))
                }
              } else if (
                expanded === 'shareLinkAccordion' &&
                shareLink &&
                isSessionWithShareURL(session)
              ) {
                model.importBookmarks(
                  await getBookmarksFromShareLink(shareLink, session.shareURL),
                )
              }
              onClose()
            } catch (e) {
              console.error(e)
              setError(e)
            }
          }}
        >
          Import
        </Button>
      </DialogActions>
    </Dialog>
  )
})
export default ImportBookmarksDialog
