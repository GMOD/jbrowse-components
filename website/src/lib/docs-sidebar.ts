import sidebarsJson from '../../sidebars.json'

export interface SidebarLink {
  type: 'link'
  label: string
  href: string
  slug: string
}

export interface SidebarGroup {
  type: 'group'
  label: string
  items: SidebarEntry[]
}

export type SidebarEntry = SidebarLink | SidebarGroup

interface DocEntry {
  id: string
  data: { title: string; sidebar_label?: string }
}

type DocusaurusSidebarItem =
  | string
  | { type: 'category'; label: string; items: DocusaurusSidebarItem[] }
  | { type: 'autogenerated'; dirName: string }
  // An explicit link to a page that isn't a docs-collection entry (e.g. the
  // auto-listed /docs/tutorials/ landing page). `to` is a doc-relative slug so
  // the base URL is applied the same way as for doc links.
  | { type: 'link'; label: string; to: string }

export function containsSlug(entry: SidebarEntry, slug: string): boolean {
  return entry.type === 'link'
    ? entry.slug === slug
    : entry.items.some(child => containsSlug(child, slug))
}

export function entrySlug(id: string): string {
  if (id === 'index') {
    return ''
  }
  return id.endsWith('/index') ? id.slice(0, -6) : id
}

function docLabel(entry: DocEntry): string {
  return entry.data.sidebar_label ?? entry.data.title
}

function docHref(baseUrl: string, slug: string): string {
  return `${baseUrl}/docs/${slug}${slug ? '/' : ''}`
}

// Splits a "<Category> -> <Name>" sidebar_label (config and state-model docs
// both emit this format) into a collapsible group per category.
function groupByCategory(links: SidebarLink[]): SidebarEntry[] {
  const grouped = new Map<string, SidebarLink[]>()

  for (const link of links) {
    const match = /^(.+?)\s*->\s*(.+)$/.exec(link.label)
    const category = match ? match[1]!.trim() : 'General'
    const label = match ? match[2]!.trim() : link.label

    if (!grouped.has(category)) {
      grouped.set(category, [])
    }
    grouped.get(category)!.push({ ...link, label })
  }

  return Array.from(grouped.entries())
    .sort((a, b) => a[0].localeCompare(b[0]))
    .map(([category, items]) => ({
      type: 'group' as const,
      label: category,
      items: items.sort((a, b) => a.label.localeCompare(b.label)),
    }))
}

function getAutoItems(
  dirName: string,
  allDocs: DocEntry[],
  baseUrl: string,
): SidebarEntry[] {
  const dirDepth = dirName.split('/').length + 1
  const links = allDocs
    .flatMap(d => {
      const slug = entrySlug(d.id)
      const parts = slug.split('/')
      if (parts.length !== dirDepth || !slug.startsWith(`${dirName}/`)) {
        return []
      }
      return [
        {
          type: 'link' as const,
          label: docLabel(d),
          href: docHref(baseUrl, slug),
          slug,
        },
      ]
    })
    .sort((a, b) => a.label.localeCompare(b.label))

  if (dirName === 'config' || dirName === 'models') {
    return groupByCategory(links)
  }
  return links
}

function convertItems(
  items: DocusaurusSidebarItem[],
  allDocs: DocEntry[],
  baseUrl: string,
  docsBySlug: Map<string, DocEntry>,
): SidebarEntry[] {
  const result: SidebarEntry[] = []

  for (const item of items) {
    if (typeof item === 'string') {
      const slug = entrySlug(item)
      const resolvedSlug = slug === 'introduction' ? '' : slug
      const entry = docsBySlug.get(resolvedSlug)
      const label = entry ? docLabel(entry) : slug
      result.push({
        type: 'link',
        label,
        href: docHref(baseUrl, resolvedSlug),
        slug: resolvedSlug,
      })
    } else if (item.type === 'autogenerated') {
      result.push(...getAutoItems(item.dirName, allDocs, baseUrl))
    } else if (item.type === 'link') {
      const slug = entrySlug(item.to)
      result.push({
        type: 'link',
        label: item.label,
        href: docHref(baseUrl, slug),
        slug,
      })
    } else {
      result.push({
        type: 'group',
        label: item.label,
        items: convertItems(item.items, allDocs, baseUrl, docsBySlug),
      })
    }
  }

  return result
}

export function buildSidebar(
  allDocs: DocEntry[],
  baseUrl: string,
): SidebarEntry[] {
  const docsBySlug = new Map(allDocs.map(d => [entrySlug(d.id), d]))
  return convertItems(
    sidebarsJson.sidebar as DocusaurusSidebarItem[],
    allDocs,
    baseUrl,
    docsBySlug,
  )
}

// The /search page groups results into a handful of broad audience filters.
// Rather than hand-list which slugs belong to which filter (a second copy of
// the categorization in sidebars.json that silently drifts whenever a page
// moves — see e.g. the 2026-07 sidebar reorg), derive it from the same
// top-level sidebar categories every doc is already placed in.
export type SearchCategory = 'user' | 'config' | 'developer' | 'autogen' | 'other'

const TOP_LEVEL_CATEGORY_BUCKETS: Record<string, SearchCategory> = {
  'User guide': 'user',
  Tutorials: 'user',
  Configuration: 'config',
  Development: 'developer',
  'API reference': 'autogen',
}
// "Embedding & scripting" and the loose cli/faq links are intentionally
// unmapped: they fall through to the always-shown 'other' bucket so no search
// filter can hide them.

// Pages whose search-filter bucket is deliberately not their sidebar category
// (e.g. admin-server setup reads as an ops/developer topic even though it's
// grouped under "Getting started" for a first-time user). Keep this list
// short — most classification should come from TOP_LEVEL_CATEGORY_BUCKETS.
const SEARCH_CATEGORY_OVERRIDES: Record<string, SearchCategory> = {
  quickstart_adminserver: 'developer',
}

function entrySlugsOf(entry: SidebarEntry): string[] {
  return entry.type === 'link' ? [entry.slug] : entry.items.flatMap(entrySlugsOf)
}

function collectSlugs(
  item: DocusaurusSidebarItem,
  allDocs: DocEntry[],
): string[] {
  if (typeof item === 'string') {
    const slug = entrySlug(item)
    return [slug === 'introduction' ? '' : slug]
  }
  if (item.type === 'link') {
    return [entrySlug(item.to)]
  }
  if (item.type === 'autogenerated') {
    return getAutoItems(item.dirName, allDocs, '').flatMap(entrySlugsOf)
  }
  return item.items.flatMap(child => collectSlugs(child, allDocs))
}

export function buildSearchCategoryMap(
  allDocs: DocEntry[],
): Record<string, SearchCategory> {
  const map: Record<string, SearchCategory> = {}
  for (const item of sidebarsJson.sidebar as DocusaurusSidebarItem[]) {
    const bucket =
      typeof item !== 'string' && item.type === 'category'
        ? TOP_LEVEL_CATEGORY_BUCKETS[item.label]
        : undefined
    if (bucket) {
      for (const slug of collectSlugs(item, allDocs)) {
        map[slug] = bucket
      }
    }
  }
  Object.assign(map, SEARCH_CATEGORY_OVERRIDES)
  return map
}
