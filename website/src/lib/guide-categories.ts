// Ordered `guide_category` values for each hand-written guide directory. A page
// declares one of these in its frontmatter; that value buckets it in both the
// landing-page index (scripts/generate-guide-indexes.ts) and the sidebar
// (docs-sidebar.ts), in the order listed here. Keeping the orders in one place
// stops the two groupings from drifting — a page's sidebar section and its
// overview-page section are guaranteed to match.

export const USER_CATEGORIES = [
  'General usage',
  'Track types',
  'Views',
  'Other features',
  'Tutorials',
]

export const CONFIG_CATEGORIES = [
  'Getting started',
  'Core configuration',
  'Track types',
  'Callbacks and customization',
  'Other features',
]

export const DEVELOPER_CATEGORIES = [
  'Getting started',
  'Core concepts',
  'Creating pluggable elements',
  'Advanced topics',
]

// Maps an autogenerated sidebar `dirName` to its category order. Only the
// hand-written guide dirs group by frontmatter `guide_category`; `config`,
// `models`, and `api` group by other means (or not at all). `tutorials` is
// deliberately absent: its sidebar list stays flat and alphabetical. The
// landing page groups the same pages into sections, which is enough
// categorization; nesting them in the sidebar too just hides pages behind
// collapsed headings.
export const GUIDE_CATEGORY_ORDER: Record<string, string[]> = {
  user_guides: USER_CATEGORIES,
  config_guides: CONFIG_CATEGORIES,
  developer_guides: DEVELOPER_CATEGORIES,
}
