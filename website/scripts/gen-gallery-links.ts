// Regenerate src/lib/galleryLinks.generated.ts from the screenshot specs so a
// gallery figure and its "open live" link always come from the one spec that
// produced the PNG (see website/CLAUDE.md). Run after editing screenshot-specs.ts:
//
//   pnpm gen:gallery-links
//
// gallery.ts imports the generated map (pure data) rather than screenshot-specs.ts
// directly, because that module's `@jbrowse/browser-test-utils` barrel pulls in
// puppeteer and can't be evaluated in the Astro/browser build.
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

import { type ScreenshotSpec, specs } from './screenshot-specs.ts'

function isUrlSpec(
  spec: ScreenshotSpec,
): spec is Extract<ScreenshotSpec, { mode: 'url' }> {
  return spec.mode === 'url'
}

const map = Object.fromEntries(
  specs.filter(isUrlSpec).map(spec => [spec.name, spec.url] as const),
)

// A raw '#' in a session URL is read as a fragment and silently truncates the
// session (e.g. a "#rrggbb" color in a hand-written spec- string). sessionSpec()
// encodes it, but raw-string url specs can reintroduce it — fail loudly here.
for (const [name, url] of Object.entries(map)) {
  if (url.includes('#')) {
    console.error(
      `spec "${name}" url contains a raw '#', which truncates the session as a URL fragment — percent-encode it (%23) or use the sessionSpec() helper`,
    )
    process.exit(1)
  }
}

const outFile = join(
  dirname(fileURLToPath(import.meta.url)),
  '../src/lib/galleryLinks.generated.ts',
)

const body = `// AUTO-GENERATED by scripts/gen-gallery-links.ts — do not edit by hand.
// Maps a screenshot-spec name to the \`?config=…&session=spec-…\` query that
// produced its committed figure, so gallery.ts opens the exact view each PNG
// shows. Regenerate with \`pnpm gen:gallery-links\` after editing
// scripts/screenshot-specs.ts.
export const specSessionUrls: Record<string, string> = ${JSON.stringify(
  map,
  null,
  2,
)}
`

// --check (for CI): fail if the committed file is stale rather than rewriting it
if (process.argv.includes('--check')) {
  const current = readFileSync(outFile, 'utf8')
  if (current !== body) {
    console.error(
      'src/lib/galleryLinks.generated.ts is stale — run `pnpm gen:gallery-links`',
    )
    process.exit(1)
  }
  console.log(`galleryLinks.generated.ts up to date (${Object.keys(map).length} urls)`)
} else {
  writeFileSync(outFile, body)
  console.log(`wrote ${Object.keys(map).length} spec urls -> ${outFile}`)
}
