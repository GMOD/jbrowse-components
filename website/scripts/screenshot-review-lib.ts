const __dirname = path.dirname(fileURLToPath(import.meta.url))

export const websiteRoot = path.resolve(__dirname, '..')
export const imgDir = path.resolve(websiteRoot, 'static', 'img')
export const reportPath = path.resolve(__dirname, 'screenshot-review.json')

// Directories scanned for doc usages of each image
const docRoots = ['docs', 'blog', 'src/pages'].map(d =>
  path.resolve(websiteRoot, d),
)

export interface DocUsage {
  file: string // relative to websiteRoot
  line: number
  caption: string // Figure caption / markdown alt text
}

// One reviewable screenshot. `autogenerated` means a generator (a
// generate-screenshots.ts spec, or a desktop capture) can reproduce it;
// otherwise it's a hand-captured image kept only in static/img (e.g. UI-flow
// walkthroughs, the gallery read-vs-ref shot) that must be re-captured by hand.
export interface Screenshot {
  name: string
  usages: DocUsage[]
  autogenerated: boolean
  exists: boolean
  existsOnMain: boolean
  // present on both this branch and origin/main but the working-tree pixels
  // differ — i.e. an update the current work introduced
  changed: boolean
}

function walkMarkdown(dir: string): string[] {
  const out: string[] = []
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name)
    if (entry.isDirectory()) {
      out.push(...walkMarkdown(full))
    } else if (/\.mdx?$/.test(entry.name)) {
      out.push(full)
    }
  }
  return out
}

const markdownFiles = docRoots
  .filter(d => fs.existsSync(d))
  .flatMap(d => walkMarkdown(d))

// Pull the caption="..." attribute out of a <Figure .../> line if present
function extractFigureCaption(line: string) {
  const m = /caption=("|')([\s\S]*?)\1/.exec(line)
  return m?.[2] ?? ''
}

// Pull the alt text out of a markdown image ![alt](src)
function extractMarkdownAlt(line: string) {
  const m = /!\[([^\]]*)\]/.exec(line)
  return m?.[1] ?? ''
}

// Every image-name a single line references. Matches the `/img/<name>.png`
// segment in any form the docs use — <Figure src="/img/x.png"/>,
// ![alt](/jb2/img/x.png), and absolute github-raw URLs that point back at this
// repo's website/static/img/x.png (used in blog posts so external aggregators
// resolve the image). Third-party github-raw images (jb2export, plugin-list)
// also match here but get dropped later because their PNG isn't on disk.
function imageNamesOnLine(line: string): string[] {
  const names: string[] = []
  const re = /\/img\/([A-Za-z0-9_/-]+)\.png/g
  let m: RegExpExecArray | null = re.exec(line)
  while (m) {
    names.push(m[1]!)
    m = re.exec(line)
  }
  return names
}

// Scan every markdown file once, building name -> usages. Re-reading per-name
// was O(names × files); this is O(files) and shared across both review tools.
function buildUsageIndex(): Map<string, DocUsage[]> {
  const index = new Map<string, DocUsage[]>()
  for (const file of markdownFiles) {
    // a doc file can be deleted/regenerated between the startup scan and now
    // (e.g. autogen rewriting docs/config/*.md); a vanished file is just not a
    // usage, so skip it rather than crashing
    const text = fs.existsSync(file) ? fs.readFileSync(file, 'utf8') : ''
    if (!text.includes('/img/')) {
      continue
    }
    const lines = text.split('\n')
    const rel = path.relative(websiteRoot, file)
    for (let i = 0; i < lines.length; i++) {
      const line = lines[i]!
      const names = imageNamesOnLine(line)
      if (names.length > 0) {
        const isFigure = line.includes('<Figure')
        const usage: DocUsage = {
          file: rel,
          line: i + 1,
          caption: isFigure
            ? extractFigureCaption(line)
            : extractMarkdownAlt(line),
        }
        for (const name of names) {
          const list = index.get(name)
          if (list) {
            list.push(usage)
          } else {
            index.set(name, [usage])
          }
        }
      }
    }
  }
  return index
}

let usageIndex: Map<string, DocUsage[]> | undefined

function getUsageIndex() {
  if (!usageIndex) {
    usageIndex = buildUsageIndex()
  }
  return usageIndex
}

function pngExists(name: string) {
  return fs.existsSync(path.join(imgDir, `${name}.png`))
}

// sha1 of a screenshot's committed PNG, used to keep a verdict valid only while
// the reviewed image is unchanged (see isVerdictStale).
export function imageHash(name: string): string | undefined {
  return hashFile(path.join(imgDir, `${name}.png`))
}

const repoRoot = path.resolve(websiteRoot, '..')

// Cache of which names exist on origin/main (populated once at startup).
let mainSet: Set<string> | undefined

function getMainSet(): Set<string> {
  if (!mainSet) {
    try {
      const out = execFileSync(
        'git',
        ['ls-tree', '--name-only', '-r', 'origin/main', 'website/static/img/'],
        { cwd: repoRoot, encoding: 'utf8' },
      )
      mainSet = new Set(
        out
          .trim()
          .split('\n')
          .filter(Boolean)
          .map(p => {
            // strip leading "website/static/img/" and trailing ".png"
            const rel = p
              .replace('website/static/img/', '')
              .replace(/\.png$/, '')
            return rel
          }),
      )
    } catch {
      mainSet = new Set()
    }
  }
  return mainSet
}

export function mainPngExists(name: string): boolean {
  return getMainSet().has(name)
}

// Names whose working-tree PNG differs from origin/main. `git diff` compares by
// blob hash, so this is exact and decode-free; it also picks up both committed
// branch edits and uncommitted regens. Untracked (brand-new) files don't appear
// here — those are the "new" set instead (existsOnMain === false).
let changedSet: Set<string> | undefined

function getChangedSet(): Set<string> {
  if (!changedSet) {
    try {
      const out = execFileSync(
        'git',
        ['diff', '--name-only', 'origin/main', '--', 'website/static/img/'],
        { cwd: repoRoot, encoding: 'utf8' },
      )
      changedSet = new Set(
        out
          .trim()
          .split('\n')
          .filter(Boolean)
          .map(p => p.replace('website/static/img/', '').replace(/\.png$/, '')),
      )
    } catch {
      changedSet = new Set()
    }
  }
  return changedSet
}

export function mainPngChanged(name: string): boolean {
  return getChangedSet().has(name)
}

export function readMainPng(name: string): Buffer | undefined {
  const gitPath = `website/static/img/${name}.png`
  try {
    return execFileSync('git', ['show', `origin/main:${gitPath}`], {
      cwd: repoRoot,
    })
  } catch {
    return undefined
  }
}

// Desktop screenshots are produced by a SEPARATE generator
// (products/jbrowse-desktop/test/screenshots.ts, a Selenium + Electron run)
// rather than generate-screenshots.ts. They have no web spec, so without this
// list the reviewer mis-classifies them as hand-captured ("manual"). Keep this
// in sync with the capture() calls in that file.
export const desktopAutogenNames = new Set([
  'desktop-landing',
  'desktop-open-genome',
  'desktop-available-genomes',
  'desktop-session',
  'desktop-add-track',
])

// The full reviewable set: every generate-screenshots spec PLUS every image a
// doc references that actually lives in static/img. The union means gallery and
// hand-captured UI-flow images get reviewed too, not just the spec-driven ones.
// A doc-referenced name with no spec and no PNG on disk is a third-party remote
// image (not ours to review) and is excluded.
//
// Blog-only figures are excluded: blog posts are historical snapshots of past
// releases, so their images aren't kept current and shouldn't surface as review
// work. A figure shared by a blog post AND a live doc (or backed by a spec)
// stays in — its non-blog usage is what makes it reviewable.
export function collectScreenshots(specs: { name: string }[]): Screenshot[] {
  const specNames = new Set(specs.map(s => s.name))
  const index = getUsageIndex()
  const names = new Set(specNames)
  for (const [name, usages] of index) {
    const liveUsages = usages.filter(u => !u.file.startsWith('blog/'))
    if (liveUsages.length > 0 && pngExists(name)) {
      names.add(name)
    }
  }
  for (const name of desktopAutogenNames) {
    names.add(name)
  }
  return [...names]
    .sort((a, b) => a.localeCompare(b))
    .map(name => ({
      name,
      usages: index.get(name) ?? [],
      autogenerated: specNames.has(name) || desktopAutogenNames.has(name),
      exists: pngExists(name),
      existsOnMain: mainPngExists(name),
      changed: pngExists(name) && mainPngChanged(name),
    }))
}

export function loadReport(): Record<string, Verdict> {
  return loadReportFile(reportPath)
}

export function saveReport(report: Record<string, Verdict>) {
  saveReportFile(reportPath, report)
}
