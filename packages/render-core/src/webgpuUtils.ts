/// <reference types="@webgpu/types" />

// Standard alpha blend state:
//   - Uses src-alpha, one-minus-src-alpha for color.
//   - Converts straight (non-premultiplied) alpha output from shaders
//     into premultiplied alpha in the framebuffer.
// Shaders MUST output straight alpha: vec4(rgb, alpha).
//
// Some passes use a 'premultiplied' blend state ({ srcFactor: 'one' }).
// Those shaders MUST output premultiplied alpha: vec4(rgb*alpha, alpha).
export const STANDARD_BLEND_STATE: GPUBlendState = {
  color: {
    srcFactor: 'src-alpha',
    dstFactor: 'one-minus-src-alpha',
    operation: 'add',
  },
  alpha: {
    srcFactor: 'one',
    dstFactor: 'one-minus-src-alpha',
    operation: 'add',
  },
}

// Per-instance data is always fed through a vertex buffer (matching Slang's
// vertex-attribute input semantics). The bind group carries only the uniform
// at binding 1 (and, optionally, a texture + sampler at 2/3). Binding index 1
// matches what the codegen emits so shaders share the same @binding(1)
// annotation regardless of whether they sample textures.

/**
 * Creates a bind group layout for uniforms at binding 1.
 * This layout is intended for use with shaders generated by Slang,
 * which typically place uniforms at binding index 1.
 * @param device - The GPUDevice.
 * @returns The GPUBindGroupLayout.
 */
export function createUniformOnlyBindGroupLayout(device: GPUDevice) {
  return device.createBindGroupLayout({
    entries: [
      {
        binding: 1, // Standard binding for uniforms generated by Slang codegen.
        visibility: GPUShaderStage.VERTEX | GPUShaderStage.FRAGMENT,
        buffer: {
          type: 'uniform' as GPUBufferBindingType,
          hasDynamicOffset: true,
        },
      },
    ],
  })
}

/**
 * Creates a bind group for uniforms at binding 1.
 * This bind group is intended for use with shaders generated by Slang,
 * which typically place uniforms at binding index 1.
 * @param device - The GPUDevice.
 * @param layout - The GPUBindGroupLayout created by createUniformOnlyBindGroupLayout.
 * @param uniformBuffer - The GPUBuffer containing the uniform data.
 * @param uniformVisibleSize - The visible size of the uniform buffer.
 * @returns The GPUBindGroup.
 */
export function createUniformOnlyBindGroup(
  device: GPUDevice,
  layout: GPUBindGroupLayout,
  uniformBuffer: GPUBuffer,
  uniformVisibleSize: number,
) {
  return device.createBindGroup({
    layout,
    entries: [
      {
        binding: 1, // Standard binding for uniforms generated by Slang codegen.
        resource: {
          buffer: uniformBuffer,
          offset: 0,
          size: uniformVisibleSize,
        },
      },
    ],
  })
}

export function createVertexBuffer(
  device: GPUDevice,
  data: ArrayBuffer | ArrayBufferView,
) {
  const buf = device.createBuffer({
    // WebGPU buffers must be at least 4 bytes in size.
    size: Math.max(data.byteLength, 4),
    usage: GPUBufferUsage.VERTEX | GPUBufferUsage.COPY_DST,
  })
  device.queue.writeBuffer(buf, 0, data)
  return buf
}

/**
 * Converts GL attribute descriptor to WebGPU vertex format string.
 * Example: { components: 2, type: 'float' } -> 'float32x2'
 * @param attr - Vertex attribute descriptor.
 * @returns GPUVertexFormat string.
 */
export function glToGpuVertexFormat(attr: {
  components: number
  type: 'float' | 'uint' | 'int'
}): GPUVertexFormat {
  const base =
    attr.type === 'uint' ? 'uint32' : attr.type === 'int' ? 'sint32' : 'float32'
  return attr.components === 1
    ? base
    : (`${base}x${attr.components}` as GPUVertexFormat)
}
