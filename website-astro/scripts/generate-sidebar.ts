/**
 * Converts website/sidebars.json (Docusaurus format) to Starlight sidebar config.
 *
 * Starlight differences from Docusaurus:
 * - autogenerate requires a `label` field: { label, autogenerate: { directory } }
 * - autogenerate cannot be mixed with slugs in the same items array; it must be
 *   its own labeled group
 * - introduction.md has `slug: /` in its frontmatter which conflicts with the
 *   Astro homepage; rename to index.md or remove the slug override in migration
 *
 * Usage:
 *   node --experimental-strip-types scripts/generate-sidebar.ts
 *
 * Output: prints the JS sidebar array to stdout; paste into astro.config.mjs
 */

import { readFileSync, readdirSync } from 'node:fs'
import { join, resolve } from 'node:path'

const docsDir = resolve(import.meta.dirname, '../../website/docs')
const sidebarsPath = resolve(import.meta.dirname, '../../website/sidebars.json')

type DocusaurusSidebarItem =
  | string
  | { type: 'category'; label: string; items: DocusaurusSidebarItem[] }
  | { type: 'autogenerated'; dirName: string }

type StarlightSidebarItem =
  | { slug: string; label?: string }
  | { label: string; items: StarlightSidebarItem[] }
  | { label: string; autogenerate: { directory: string } }

function getFilesInDir(dir: string): string[] {
  try {
    return readdirSync(join(docsDir, dir)).filter(
      f => f.endsWith('.md') || f.endsWith('.mdx'),
    )
  } catch {
    return []
  }
}

/**
 * In Starlight, autogenerate must be its own labeled group. When a Docusaurus
 * category mixes explicit items with autogenerated items, we split them.
 */
function convertItems(items: DocusaurusSidebarItem[]): StarlightSidebarItem[] {
  const result: StarlightSidebarItem[] = []

  for (const item of items) {
    if (typeof item === 'string') {
      result.push({ slug: item })
    } else if (item.type === 'autogenerated') {
      // Becomes a labeled autogenerate group; label derived from dirName
      const label = dirLabel(item.dirName)
      result.push({ label, autogenerate: { directory: item.dirName } })
    } else if (item.type === 'category') {
      const hasAutogenerate = item.items.some(i => typeof i !== 'string' && i.type === 'autogenerated')
      const manualItems = item.items.filter(
        i => typeof i === 'string' || i.type !== 'autogenerated',
      )
      const autoItems = item.items.filter(
        i => typeof i !== 'string' && i.type === 'autogenerated',
      ) as { type: 'autogenerated'; dirName: string }[]

      if (!hasAutogenerate) {
        result.push({ label: item.label, items: convertItems(item.items) })
      } else if (manualItems.length === 0) {
        // Entire category is autogenerated
        for (const auto of autoItems) {
          result.push({
            label: item.label,
            autogenerate: { directory: auto.dirName },
          })
        }
      } else {
        // Mixed: split into manual group + autogenerate group(s)
        result.push({ label: item.label, items: convertItems(manualItems) })
        for (const auto of autoItems) {
          result.push({
            label: dirLabel(auto.dirName),
            autogenerate: { directory: auto.dirName },
          })
        }
      }
    }
  }

  return result
}

function dirLabel(dirName: string): string {
  return dirName
    .split('/')
    .at(-1)!
    .replace(/_/g, ' ')
    .replace(/\b\w/g, c => c.toUpperCase())
}

function serializeItem(item: StarlightSidebarItem, indent: number): string {
  const pad = ' '.repeat(indent)
  if ('slug' in item) {
    return item.label
      ? `${pad}{ slug: '${item.slug}', label: '${item.label}' }`
      : `${pad}{ slug: '${item.slug}' }`
  }
  if ('autogenerate' in item) {
    return `${pad}{ label: '${item.label}', autogenerate: { directory: '${item.autogenerate.directory}' } }`
  }
  const children = item.items.map(i => serializeItem(i, indent + 2)).join(',\n')
  return `${pad}{\n${pad}  label: '${item.label}',\n${pad}  items: [\n${children},\n${pad}  ],\n${pad}}`
}

const { sidebar: docusaurusSidebar } = JSON.parse(
  readFileSync(sidebarsPath, 'utf-8'),
) as { sidebar: DocusaurusSidebarItem[] }

const starlightSidebar = convertItems(docusaurusSidebar)

console.log('// Generated by scripts/generate-sidebar.ts')
console.log('// Paste this array as the `sidebar` value in astro.config.mjs\n')
console.log('sidebar: [')
for (const item of starlightSidebar) {
  console.log(serializeItem(item, 2) + ',')
}
console.log('],')
