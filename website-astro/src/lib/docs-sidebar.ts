import sidebarsJson from '../../../website/sidebars.json'

export interface SidebarLink {
  type: 'link'
  label: string
  href: string
  slug: string
}

export interface SidebarGroup {
  type: 'group'
  label: string
  items: SidebarItem[]
}

export type SidebarItem = SidebarLink | SidebarGroup

type DocEntry = { id: string; data: { title: string; sidebar_label?: string } }

type DocusaurusSidebarItem =
  | string
  | { type: 'category'; label: string; items: DocusaurusSidebarItem[] }
  | { type: 'autogenerated'; dirName: string }

function entrySlug(id: string): string {
  if (id === 'index') return ''
  return id.endsWith('/index') ? id.slice(0, -6) : id
}

function docLabel(entry: DocEntry): string {
  return entry.data.sidebar_label ?? entry.data.title
}

function getAutoItems(dirName: string, allDocs: DocEntry[]): SidebarLink[] {
  return allDocs
    .filter(d => {
      const slug = entrySlug(d.id)
      const parts = slug.split('/')
      return parts.length === dirName.split('/').length + 1 && slug.startsWith(dirName + '/')
    })
    .sort((a, b) => docLabel(a).localeCompare(docLabel(b)))
    .map(d => {
      const slug = entrySlug(d.id)
      return { type: 'link' as const, label: docLabel(d), href: slug, slug }
    })
}

function convertItems(
  items: DocusaurusSidebarItem[],
  allDocs: DocEntry[],
  baseUrl: string,
): SidebarItem[] {
  const result: SidebarItem[] = []
  const docsBySlug = new Map(allDocs.map(d => [entrySlug(d.id), d]))

  for (const item of items) {
    if (typeof item === 'string') {
      const slug = item.endsWith('/index') ? item.slice(0, -6) : item
      const resolvedSlug = slug === 'introduction' ? '' : slug
      const entry = docsBySlug.get(resolvedSlug)
      const label = entry ? docLabel(entry) : slug
      result.push({
        type: 'link',
        label,
        href: `${baseUrl}/docs/${resolvedSlug}${resolvedSlug ? '/' : ''}`,
        slug: resolvedSlug,
      })
    } else if (item.type === 'autogenerated') {
      const autoItems = getAutoItems(item.dirName, allDocs).map(l => ({
        ...l,
        href: `${baseUrl}/docs/${l.slug}/`,
      }))
      for (const ai of autoItems) {
        result.push(ai)
      }
    } else if (item.type === 'category') {
      result.push({
        type: 'group',
        label: item.label,
        items: convertItems(item.items, allDocs, baseUrl),
      })
    }
  }

  return result
}

export function buildSidebar(allDocs: DocEntry[], baseUrl: string): SidebarItem[] {
  return convertItems(sidebarsJson.sidebar as DocusaurusSidebarItem[], allDocs, baseUrl)
}
