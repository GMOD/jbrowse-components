/**
 * Generates the Starlight sidebar config from website/sidebars.json.
 * Edit scripts/generate-sidebar.ts to change conversion rules.
 */
import { readFileSync } from 'node:fs'
import { resolve } from 'node:path'

const sidebarsPath = resolve(import.meta.dirname, '../../website/sidebars.json')

type DocusaurusSidebarItem =
  | string
  | { type: 'category'; label: string; items: DocusaurusSidebarItem[] }
  | { type: 'autogenerated'; dirName: string }

type StarlightSidebarItem =
  | { slug: string; label?: string }
  | { label: string; items: StarlightSidebarItem[] }
  | { label: string; autogenerate: { directory: string } }

function dirLabel(dirName: string): string {
  return dirName
    .split('/')
    .at(-1)!
    .replace(/_/g, ' ')
    .replace(/\b\w/g, c => c.toUpperCase())
}

function convertItems(items: DocusaurusSidebarItem[]): StarlightSidebarItem[] {
  const result: StarlightSidebarItem[] = []

  for (const item of items) {
    if (typeof item === 'string') {
      // Astro strips trailing `/index` from directory index files
      const slug = item.endsWith('/index') ? item.slice(0, -'/index'.length) : item
      result.push({ slug })
    } else if (item.type === 'autogenerated') {
      result.push({ label: dirLabel(item.dirName), autogenerate: { directory: item.dirName } })
    } else if (item.type === 'category') {
      const manualItems = item.items.filter(
        i => typeof i === 'string' || i.type !== 'autogenerated',
      )
      const autoItems = item.items.filter(
        i => typeof i !== 'string' && i.type === 'autogenerated',
      ) as { type: 'autogenerated'; dirName: string }[]

      if (autoItems.length === 0) {
        result.push({ label: item.label, items: convertItems(item.items) })
      } else if (manualItems.length === 0) {
        for (const auto of autoItems) {
          result.push({ label: item.label, autogenerate: { directory: auto.dirName } })
        }
      } else {
        // Mixed: manual items become their own group, autogenerate becomes a sibling group
        result.push({ label: item.label, items: convertItems(manualItems) })
        for (const auto of autoItems) {
          result.push({ label: dirLabel(auto.dirName), autogenerate: { directory: auto.dirName } })
        }
      }
    }
  }

  return result
}

const { sidebar: docusaurusSidebar } = JSON.parse(
  readFileSync(sidebarsPath, 'utf-8'),
) as { sidebar: DocusaurusSidebarItem[] }

export const sidebar = convertItems(docusaurusSidebar)
